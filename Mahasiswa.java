
public class Mahasiswa {
        String nama ;

        
        public Mahasiswa(String nama){
            this.nama = nama;
        }
        
        public void membaca(){
            System.out.println(nama + " Sedang membaca buku");                                                                             
        }
        
         public void nyontek(){
            System.out.println(nama + " Sedang nyontek dari buku Afwan");                                                                             
        }
         
         public void modifikasi(){
            System.out.println(nama + " Sedang memodifikasi jawaban dari afwan");                                                                             
        }
         
        
        
}
